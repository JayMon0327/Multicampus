package 복습;

public class 기본형심화2 {

	public static void main(String[] args) {
		//정수형 4가지
		//-128~127, 1바이트 byte
		//-3만~3만, 2바이트 short
		//-21억~21억, 4바이트 int
		//21억 이상은, 8바이트 long
		
		byte age = 127;
		short days = 1000;
		int money = 100000; //**
		long bank = 3124124L; // 뒤에 L 써야함
		
		
		//실수형 2가지
		//소수점 4자리까지 4바이트 float
		float height = 170.4f; // 뒤에 f,F 써야함
		//소수점 4자리이상 8바이트 double
		double weight = 43.33333333;
		
		
		//문자 1 char
		//논리형 boolean
	}

}
