package com.multi.mvc033;

public class mapVO {

	private double we;
	private double are;
	
	public double getWe() {
		return we;
	}
	public void setWe(double we) {
		this.we = we;
	}
	public double getAre() {
		return are;
	}
	public void setAre(double are) {
		this.are = are;
	}
	@Override
	public String toString() {
		return "mapVO [we=" + we + ", are=" + are + "]";
	}
	
}